import sys
sys.set_int_max_str_digits(2000000) #Так як числа у нас на вході дуже великі, треба зняти обмеження з пітона

"""
Ми ж знаєм шо алгоритм карацуби для великих чисел, тому я взяв на себе сміливість зробити відхід
 від алгоритма карацуби коли маємо справу з досить малими числами.
 (це можна змінювати за константною змінною RECURSIVE_STOP
 
 Також юзаємо двійкові операції, бо операції над рядками будуть
  капець як сповільнювати наш алгоритм
"""
RECURSIVE_STOP = 2048

def karatsuba_speedup(x: int, y: int) -> int:
    # Тут нам краще обмежити рекурсію, бо вона дуже сповільнює справу
    if x < RECURSIVE_STOP or y < RECURSIVE_STOP:
        return x * y

    # Знаходимо довжину в бітах
    m = max(x.bit_length(), y.bit_length()) // 2

    # 3. Блискавичне розбиття числа маскою та зсувом
    # Замість a, b = x[:m], x[m:]
    mask = (1 << m) - 1
    low1 = x & mask  # Права частина
    high1 = x >> m  # Ліва частина

    low2 = y & mask
    high2 = y >> m

    # 4. Ті самі три рекурсивні виклики
    z0 = karatsuba_speedup(low1, low2)
    z1 = karatsuba_speedup(low1 + high1, low2 + high2)
    z2 = karatsuba_speedup(high1, high2)

    # 5. Збираємо результат побітовими зсувами (замість додавання нулів у рядок)
    return (z2 << (2 * m)) + ((z1 - z2 - z0) << m) + z0


# ===== використання =====
A, B = input().split()

# Конвертуємо рядки в числа ТІЛЬКИ ОДИН РАЗ на самому початку
print(karatsuba_speedup(int(A), int(B)))