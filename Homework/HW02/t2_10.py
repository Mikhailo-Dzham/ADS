def f(n):
    sum = 0  # O(2)

    # Цикл виконується рівно n разів.
    # Кожна ітерація циклу — це O(1).
    # Загалом для циклу: n * O(4) = O(n)
    for i in range(1, n + 1):
        sum = sum + i  # O(4)

    return sum  # O(2)

# Беремо найбільше O велике, O(n) що й буде нашою асимптотичною швидкістю